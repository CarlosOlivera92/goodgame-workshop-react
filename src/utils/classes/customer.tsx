class Customer {
    name: any;
    address: any;
    emailAddress: any;
    phoneNumber: any;
    constructor(name, lastName, address, emailAddress, phoneNumber) {
        this.name = name;
        this,lastName = lastName;
        this.address = address;
        this.emailAddress = emailAddress;
        this.phoneNumber = phoneNumber;
    }
}
export default Customer;